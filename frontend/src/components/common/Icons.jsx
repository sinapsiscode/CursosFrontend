export const getUserRoleIcon = (role) => {
  switch (role) {
    case 'admin':
      return <AdminIcon />
    case 'user':
      return <UserIcon />
    default:
      return null
  }
}

export const getTypeIcon = (type) => {
  switch (type) {
    case 'pdf':
      return <PdfIcon />
    case 'video':
      return <VideoIcon />
    default:
      return <DocumentIcon />
  }
}